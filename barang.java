        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtkdbarang.getText());
            stat.setString(2, txtnmbarang.getText());
            stat.setString(3, txthrgbarang.getText());
            stat.setString(4, txthrgjual.getText());
            stat.setString(5, jumlah.getText());
            stat.setString(6, (String)cb1.getSelectedItem());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
          //  autonomor();
            txtkdbarang.requestFocus();
            datatable();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan "+e);
        }         // TODO add your handling code here:
